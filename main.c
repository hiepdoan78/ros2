void DC_speed(float SPEED){
  int target_pwm = SPEED *999/22;
	if(target_pwm > 999) target_pwm = 999;
	if(target_pwm < 500) target_pwm = 500;
  PWM = target_pwm;
  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, PWM);
  
}
void sendspeed(float steering, double encoder) { 
	HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
  snprintf(data, sizeof(data), "%.3f;%.3f\n", steering, encoder);
  HAL_UART_Transmit(&huart1, (uint8_t*)data, strlen(data),20);
	memset(data, 0, sizeof(data));
}
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if (htim->Instance == TIM2){
		if (counter > 0){
			counter--;
		if (counter <= 0){
			counter = 5;
			counter_flag = 1;
			}
		}
	}
}
void Set_Servo_Position(float position) {
	if(position == 0){
		angle_deg = 0;
	}else{
	float inner_position = atan(1.0f / ((1.0f / tan(position)) - track / (2.0f * wheelbase)));
	angle_deg = inner_position * (180.0 / 3.14159); 
	}
  if (angle_deg < -26.5) angle_deg = -26.5;
  if (angle_deg > 35) angle_deg = 35;

  float pulse_length = (angle_deg * -157.0f / 300.0f) + 75.0f;
  htim1.Instance->CCR1 = 150 - pulse_length;
}

void LiftTruck(int state){
	switch(state){
		case 2:
			HAL_GPIO_WritePin(IN3_GPIO_Port, IN3_Pin, GPIO_PIN_SET);
			HAL_GPIO_WritePin(IN4_GPIO_Port, IN4_Pin, GPIO_PIN_RESET);
			break;
		case 3:
			HAL_GPIO_WritePin(IN3_GPIO_Port, IN3_Pin, GPIO_PIN_RESET);
			HAL_GPIO_WritePin(IN4_GPIO_Port, IN4_Pin, GPIO_PIN_SET);
			break;
		default: break;
	}
	
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if (huart->Instance == USART1) {
    rx_buffer[rx_index++] = rx_char;  

    if (rx_char == '\n') {
      data_ready = 1;  
      rx_index = 0; 
    }

    HAL_UART_Receive_DMA(&huart1, &rx_char, 1);
  }
}


void Set_Motor_Speed(float joint_speed) {
	if (speed > 0) {
		HAL_GPIO_WritePin(IN1_GPIO_Port, IN1_Pin, GPIO_PIN_SET);
		HAL_GPIO_WritePin(IN2_GPIO_Port, IN2_Pin, GPIO_PIN_RESET);
		DC_speed(speed);

  } else if (speed < 0) {
		HAL_GPIO_WritePin(IN1_GPIO_Port, IN1_Pin, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(IN2_GPIO_Port, IN2_Pin, GPIO_PIN_SET);
		DC_speed(fabs(speed));

  } else if (speed == 0) {
		HAL_GPIO_WritePin(IN1_GPIO_Port, IN1_Pin, GPIO_PIN_SET);
		HAL_GPIO_WritePin(IN2_GPIO_Port, IN2_Pin, GPIO_PIN_SET);
  }
}

